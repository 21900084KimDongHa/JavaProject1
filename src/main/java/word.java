public class word {
    private int id;
    private int lv;
    private String word;
    private String mean;

    public int getId() {
        return id;
    }

    public int getLv() {
        return lv;
    }

    public String getWord() {
        return word;
    }

    public String getMean() {
        return mean;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setLv(int lv) {
        this.lv = lv;
    }

    public void setWord(String word) {
        this.word = word;
    }

    public void setMean(String mean) {
        this.mean = mean;
    }
}
